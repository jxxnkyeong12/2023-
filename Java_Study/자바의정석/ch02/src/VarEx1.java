
public class VarEx1 {

	public static void main(String[] args) {
			int x = 6, y =3 ;
			int result = 0;
		    result = x+y;	
		System.out.println(result);
		System.out.println("덧셈 :: " + (x+y));	//덧셈
		System.out.println("뺄셈 :: " + (x-y));	//뺄셈
		System.out.println("곱하기 :: " + x*y);	//곱하기
		System.out.println("나누기 :: " + x/y);	//나누기
	}

}
